"""
Write an algorithm to print all ways of arranging eights queens on an 8x8 chess board so that none of them share the same
row, column, or diagonal. In this case, "diagonal" means all diagonals, not just the two that bisect the board.
"""
import unittest
from copy import deepcopy

def position_is_valid(board, n):
    queen_positions = []
    for i in range(n):
        for j in range(n):
            if board[i][j] == "Q":
                # row is i ("y"), col is j ("x")
                queen_positions.append((j, i))
    x_points = [point[0] for point in queen_positions]
    if len(set(x_points)) != len(x_points):
        return False
    for i in range(len(queen_positions)):
        for j in range(i):
            if i == j:
                continue
            point = queen_positions[i]
            point_2 = queen_positions[j]
            if abs((point_2[1] - point[1]) / (point_2[0] - point[0])) == 1:
                return False
    return True

def position_is_valid_faster(i, j, board, n):
    # check if any queens in rows above this position in the same column
    # only have to check above since we haven't gotten to the rows below yet
    for row in range(0, i):
        if board[row][j] == "Q":
            return False
    # check if any queens in cols to the left or right of this position in the same row, including this position
    # This is a sanity check since our main function shouldn't add more tha one queen at any row
    for col in range(0, n):
        if board[i][col] == "Q":
            return False

    # check if any queens in positive (upper right) diagonal
    # only have to check upper diagonal since we haven't gotten to the rows below yet
    row = i
    col = j
    while row >= 0 and col < n:
        if board[row][col] == "Q":
            return False
        row -= 1
        col += 1
    # check if any queens in negative (upper left) diagonal
    # only have to check upper diagonal since we haven't gotten to the rows below yet
    row = i
    col = j
    while row >= 0 and col >= 0:
        if board[row][col] == "Q":
            return False
        row -= 1
        col -= 1
    return True



    
def queens(n=8):
    arrangements = []
    def helper(board, i=0):
        if i == n:
            # we made it this far, the arrangement is valid
            arrangements.append(board)
        else:
            for j in range(n):
                # check if position is valid, and if it is then add a queen and advance to next row
                #if position_is_valid(board, n):
                if position_is_valid_faster(i, j, board, n):
                    board[i][j] = "Q"
                    helper(deepcopy(board), i+1)
                # reset the position so we can add a queen to the next position in the row
                board[i][j] = "."

    board = [["." for _ in range(8)] for _ in range(8)]
    helper(board)
    return arrangements

